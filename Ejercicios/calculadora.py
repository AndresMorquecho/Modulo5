alto = float(input("Ingrese el Alto del rectangulo: "))
ancho = float(input("Ingrese el Ancho del rectangulo: "))

area_rectangulo = alto * ancho

print(f"El area del rectangulo es: {area_rectangulo}")

perimeto = 2 * (alto + ancho)

print(f"El perimeto del reactangulo es: {perimeto}")

superifice =  alto * ancho

print(f"La superficie del rectangulo es: {superifice}")
